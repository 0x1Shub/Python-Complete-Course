course = "Python for Beginneers"

print(course)

# Converts all characters to uppercase. It not modify our string it return new strings
print(course.upper())

# Converts all characters to lowercase
print(course.lower())

# Find that Character
print(course.find('y'))
print(course.find('Y'))
print(course.find("for"))

# Replace the character or word. It not modify our string it return new strings
print(course.replace('for', '4'))

# use in method to find the word 
print('for' in course)

print(course)

