print(10/3) # The single / or division will provide floating value as output

print(10 // 3) # The double // or division will provide the whole number

print(10 % 3) # The module operator will provide remainder

print(10 * 3) # The Single * provide multiplication

print(10 ** 3) # The double ** provide the power function the 10 power of 3

